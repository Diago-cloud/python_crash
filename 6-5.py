river = {
    'China': 'HuangHe',
    'American': 'Mixixib',
    'Banama': 'Banama yunhe',
    'nile': 'egypt'
}

for key, value in river.items():
    print("The " + value + " runs through " + key)

print("\n")

for keys in river.keys():
    print(keys)

print("\n")

for values in river.values():
    print(values)
